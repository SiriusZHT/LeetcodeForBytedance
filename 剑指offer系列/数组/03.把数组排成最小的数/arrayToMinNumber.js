/**
 * 输入一个正整数数组，把数组里所有数字拼接起来排成一个数，打印能拼接出的所有数字中最小的一个。
 * 例如输入数组{3，32，321}，则打印出这三个数字能排成的最小数字为 321323。
 * 因为涉及拼接，所以可以将其看做字符串，同时规避了大数溢出的问题，而且字符串的比较规则和数字相同。
借助自定义排序，可以快速比较两个数的大小。比如只看{3, 32}这两个数字。它们可以拼接成 332 和 323，按照题目要求,332 > 323，这里应该取 323。
也就是说，此处自定义函数应该返回1。
 * @param {*} arr 
 */
function arrayToMinNumber(arr){
    return arr.sort((a, b) => (a + "" + b) > (b + "" + a) ? 1 : -1).join("");
}

